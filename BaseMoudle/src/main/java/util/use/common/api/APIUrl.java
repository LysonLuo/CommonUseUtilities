public class APIUrl {
    public static final String API_URL = "";

    /**
     * API地址
     *
     * @return
     */
    public static String baseAPIUrl() {
        return API_URL + "";
    }

    /**
     * 网页地址
     *
     * @return
     */
    public static String baseWebUrl() {
        return API_URL;
    }
}
